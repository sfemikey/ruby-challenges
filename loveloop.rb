A Loop for Love

all_love_notes = [
  "Love you to the moon and back!",
  "You are all I love, all I want and all I need.",
  "Love moves in mysterious ways.",
  "I want all of you, forever!",
  "Your love I cherished the most."
]

total_love_notes = all_love_notes.size
love_notes displayed = 0
while (love_notes_displayed <= total_love_notes)
  puts all_love_notes[love_notes_displayed]
  love_notes_displayed += 1
  puts love_notes_displayed
end
