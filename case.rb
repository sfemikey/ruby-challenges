fav_color = 'teal'

case fav_color
when 'red'
	puts "Red like fire!"
when 'orange'
	puts "Orange like,well... an orange."
when 'yellow')
	puts "Yellow as bright as the Sun!"
when 'green')
	puts "Have you been to the Emerald  City in Oz?"
when 'blue')
	puts "Blue like the sky!"
when 'purple')
	puts "Purple lilacs are the prettiest."
else
	puts "Hmm, well I don't know what the color is!"
end


weather = 'cloudy'

case weather
when 'sunny'
	puts "Make sure to put sunscreen and wear your cool shade"
when 'windy'
	puts "Wear a hat, it will be windy today!"
when 'snowy'
	puts "You better wear your snow pants and boots! It will be snowy outside!"
when 'rainy'
	puts "Don't forget your umbrella!"
when 'cloudy'
	puts "It might be cool outside, wear a sweater!"
else
	puts "Hmm, not sure what type of clothes you need to wear in this kind of weather."
end
